#latihan operator pada python
#operator aritmatika untuk penjumlahan
bilangan1 = int(input("Masukkan bilangan pertama: ")) #string
bilangan2 = int(input("Masukkan bilangan kedua: ")) #string

#membaca data yang diinput pengguna
print("---------------------------")
print("bilangan 1 = ", bilangan1)
print("bilangan 2 = ", bilangan2)
print("---------------------------")

#operator penjumlaahn
hasilPenjumlahan = bilangan1 + bilangan2
print("hasil penjumlahan bilangan 1 + bilangan 2 = ", hasilPenjumlahan)
print("---------------------------")


